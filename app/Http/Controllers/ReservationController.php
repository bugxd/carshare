    {
        if (Auth::check()) {
            $reservation = Reservation::create([
                'CID' => Car::where('licence_plate', $request->input('plate')->id),
                'UID' => Auth::user()->id]);

            if ($reservation) {
                return 'Auto wurde erfolgreich erstellt';
            }

            return 'Es ist ein Fehler aufgetreten';
        }
    }

